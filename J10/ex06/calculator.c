#include "calculator.h"

int		ft_add(int nbr1, int nbr2)
{
	return (nbr1 + nbr2);
}

int		ft_minus(int nbr1, int nbr2)
{
	return (nbr1 - nbr2);
}

int		ft_mul(int nbr1, int nbr2)
{
	return (nbr1 * nbr2);
}

int		ft_div(int nbr1, int nbr2)
{
	return (nbr1 / nbr2);
}

int		ft_mod(int nbr1, int nbr2)
{
	return (nbr1 % nbr2);
}
